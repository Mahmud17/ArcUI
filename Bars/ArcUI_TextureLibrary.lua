-- Flat id -> friendly name lookup (id may be a number or an atlas string).
ns.TextureLibrary.nameById = {}
for _, cat in ipairs(ns.TextureLibrary.categories) do
  for _, entry in ipairs(cat.textures) do
    ns.TextureLibrary.nameById[entry.id] = entry.name
  end
end

-- Returns the friendly name for a library id, or a sensible fallback string.
function ns.TextureLibrary.GetName(id)
  if id == nil then return "" end
  local n = ns.TextureLibrary.nameById[id]
  if n then return n end
  if type(id) == "string" then return id end
  return tostring(id)
end
